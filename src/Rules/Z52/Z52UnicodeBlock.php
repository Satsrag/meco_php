{
    private static $z52Codes = null;
    private static $z52Punctuations = null;

    public static function isZ52Code($cp)
    {
        if (self::$z52Codes === null) {
            self::$z52Codes = array_flip([
                0x1865, 0x1866, 0x1867, 0x1868, 0x1869, 0x186A, 0x186B, 0x186C, 0x186D,
                0x186E, 0x186F, 0x1870, 0x1871, 0x1872, 0x1873, 0x1874, 0x1875, 0x1876,
